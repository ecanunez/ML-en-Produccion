def draw_icon(
    dwg,
    icon_name,
    cx,
    cy,
    size=55,
):
    base_size = 256 if icon_name == "ball" else 24

    scale = size / base_size

    x = cx - size / 2
    y = cy - size / 2

    group = dwg.g(
        transform=f"translate({x},{y}) scale({scale})"
    )

    for item in ICONS[icon_name]:
        kind = item[0]

        if kind == "path":
            group.add(
                dwg.path(
                    d=item[1],
                    fill="none",
                    stroke=PRIMARY,
                    stroke_width=2,
                    stroke_linecap="round",
                    stroke_linejoin="round",
                )
            )

        elif kind == "filled_path":
            group.add(
                dwg.path(
                    d=item[1],
                    fill=PRIMARY,
                    stroke="none",
                )
            )

        elif kind == "circle":
            cx0, cy0, r = item[1]
            group.add(
                dwg.circle(
                    center=(cx0, cy0),
                    r=r,
                    fill="none",
                    stroke=PRIMARY,
                    stroke_width=2,
                )
            )

        elif kind == "rect":
            x0, y0, w, h, r = item[1]
            group.add(
                dwg.rect(
                    insert=(x0, y0),
                    size=(w, h),
                    rx=r,
                    ry=r,
                    fill="none",
                    stroke=PRIMARY,
                    stroke_width=2,
                )
            )

    dwg.add(group)